    private void txtNombreGrupoKeyPressed(java.awt.event.KeyEvent evt)//GEN-FIRST:event_txtNombreGrupoKeyPressed
    {//GEN-HEADEREND:event_txtNombreGrupoKeyPressed
        // TODO add your handling code here:
        if (txtNombreGrupo.getText().trim().length() > 2)
        {
            txtNombreGrupo.setBorder(txtColorNormal.getBorder());
        }else
        {
            txtNombreGrupo.setBorder(txtColorRojo.getBorder());
        }

        if (evt.getKeyChar() == '\n')
        {
            btnAgregarActionPerformed(null);
        }
    }//GEN-LAST:event_txtNombreGrupoKeyPressed

    
